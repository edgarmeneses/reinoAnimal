public enum TipoGrupo {
	/**
	 * tiene pelos en la superficie del cuerpo. En la mayoría de las especies de
	 * mamíferos, no en todas, las hembras poseen mamas por las que sale la leche con
	 * que alimentan a sus crías.
	 */
	mamiferos,
	/**
	 * son animales vertebrados, de sangre caliente, que caminan, saltan o se
	 * mantienen sólo sobre las extremidades posteriores, mientras que las
	 * extremidades anteriores están modificadas como alas
	 */
	aves,
	/**
	 * los peces son animales vertebrados acuáticos, recubiertos en su mayoría de
	 * escamas y dotados de aletas, que permiten su desplazamiento en el medio acuático
	 */
	peces,
	/**
	 * los reptiles son animales vertebrados. Se estima que hace unos 310 millones de
	 * años descendieron de los anfibios
	 */
	reptiles,
	/**
	 * los anfibios son un grupo de vertebrados, con respiración branquialdurante la
	 * fase larvaria y pulmonar al alcanzar el estado adulto
	 */
	anfibios
}
public enum TipoReproduccion {
	/**
	 * los animales ovíparos se forman y desarrollan en el interior de un huevo que ha
	 * fertilizado el macho de la especie y ha sido puesto por la hembra.
	 */
	oviparos,
	/**
	 * los vivíparos son animales que se desarrollan dentro del útero o matriz, que es
	 * un órgano que está en el aparato reproductor de la hembra y al nacer, pasan y
	 * salen por el canal vaginal
	 */
	viviparos,
	/**
	 * se forman dentro de huevos que están dentro de la madre y en el trayecto
	 * uterino se va rompiendo el cascarón y nacen directamente de la madre
	 */
	oviviparos
}